
// Q.5 given task of building a function that convert tempreture value
// from fahrenheit to celsius
// the formula to convert fahrenheit tocelsius
// celsius value = (fahrenite value) * 5/9



function convertCelsius(fahrenheit) {

    celsius = (fahrenheit - 32) * 5 / 9

    return celsius

}
console.log(convertCelsius(56));



// Q.6 You have been given the task of building a function that 
// calculates the area of a circle based on its radius.
// The formula to calculate the area of a circle is as follows:
// Area = 3.14 * radius * radius 


function area_of_circle(r) {

    let PI = 3.14;
    let area = PI * r * r
    return area

}
console.log(area_of_circle(5))





